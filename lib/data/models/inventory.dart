class Inventory {
  final String id;
  final String name;
  final String description;
  final String type;
  final String use;
  final int rColor;
  final int gColor;
  final int bColor;

  const Inventory({
    required this.id,
    required this.name,
    required this.description,
    required this.type,
    required this.use,
    required this.rColor,
    required this.gColor,
    required this.bColor,
  });
}
